<script>
  $(document).ready(function() {
    // Function to fetch and display greetings
    function fetchGreetings() {
      $.ajax({
        url: "{{ route('greetings.index') }}",
        method: "GET",
        success: function(response) {
          let greetingsContainer = $('#greetingsContainer');
          greetingsContainer.empty(); // Clear existing content

          response.forEach(function(greeting) {
            let timeAgo = moment(greeting.created_at).fromNow(); // Use moment.js to display time ago
            let chatBubble = `
              <div class="chat-bubble">
                <strong>${greeting.name}</strong>
                <p>${greeting.message}</p>
                <div class="time-ago">${timeAgo}</div>
              </div>`;
            greetingsContainer.append(chatBubble);
          });
        },
        error: function() {
          alert('Gagal mengambil data ucapan.');
        }
      });
    }

    // Initial fetch of greetings
    fetchGreetings();

    $('#greetingForm').on('submit', function(event) {
      event.preventDefault();

      $.ajax({
        url: "{{ route('submit.greeting') }}",
        method: "POST",
        data: {
          _token: '{{ csrf_token() }}',
          name: $('#name').val(),
          message: $('#message').val()
        },
        success: function(response) {
          $('#greetingForm')[0].reset();
          fetchGreetings(); // Refresh the greetings list
        },
        error: function() {
          alert('Terjadi kesalahan. Silakan coba lagi.');
        }
      });
    });
});
</script>
